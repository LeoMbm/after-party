
class Reservations {
    constructor(id, checkIn, checkOut, guests, price, createdAt, updatedAt) {
        this.id = id;
        this.checkIn = checkIn;
        this.checkOut = checkOut;
        this.guests = guests;
        this.price = price;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }
}