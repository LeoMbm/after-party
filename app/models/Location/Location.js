class Location {
    constructor(id, name, address, longitude, latitude ,phone, createdAt, updatedAt, data) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.longitude = longitude;
        this.latitude = latitude;
        this.phone = phone;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
        this.data = data;
    }

}